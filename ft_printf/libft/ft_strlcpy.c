#include "libft.h"

size_t	ft_strlcpy(char *d, const char *s, size_t n)
{
	size_t	i;

	if (!d)
		return (0);
	i = 0;
	while (s[i] != 0 && i < n - 1)
	{
		if (n != 0)
			d[i] = s[i];
		i++;
	}
	if (n != 0)
		d[i] = 0;
	i = 0;
	while (s[i] != 0)
		i++;
	return (i);
}
